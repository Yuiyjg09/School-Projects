package questions;

public class Placeholder {
	private String text;
	private int num;
	
	public Placeholder(String text, int num) {
		this.text = text;
		this.num = num;
	}
	
	public int getNum() {
		return num;
	}
	
	public String getText() {
		return text;
	}
	
	public void setNum(int num) {
		this.num = num;
	}
	
	public void setText(String text) {
		this.text = text;
	}
}
